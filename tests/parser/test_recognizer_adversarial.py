# Finding 14 -- post-graph text clusters: sticky/optional 'set hei'/'set
# color'/'set just', and string fidelity for backslash+brace content.
# --------------------------------------------------------------------------- #
#
# The writer (writer.py GLEWriter.add_text) always restates 'set color' and
# 'set just' for every text and conditionally restates 'set hei' -- but a
# hand-written (or hand-edited) .gle file may rely on GLE's real interpreter
# semantics instead: 'set hei'/'set color'/'set just' are GLOBAL state that
# persists across statements until changed again. A cluster may therefore
# omit any subset of the three 'set' lines and inherit whatever was last in
# effect. Before this fix, _try_one_text() required 'set color' and 'set
# just' on EVERY cluster and re-initialized fontsize/color/just to hardcoded
# defaults on each attempt (no memory across clusters) -- so any cluster
# missing 'set color' or 'set just' failed to match, and _consume_text_cluster
# aborts its ENTIRE loop on the first failed cluster, dumping the whole
# remainder (including already-matched-looking earlier lines) into
# fig.passthrough_trailer verbatim instead of ax.texts.

def test_finding14_sticky_state_recovers_three_unevenly_annotated_clusters(tmp_path):
    """The reported real-world shape: a single 'set hei' shared by multiple
    clusters, a repeated (redundant) 'set just left', and a THIRD cluster with
    no 'set just' (or 'set hei'/'set color') at all. All three must land in
    ax.texts with the sticky-inherited fontsize/color/ha, in order, and the
    third text's braces+backslash content must survive byte-exact.
    """
    src = (
        "size 10 8\n"
        "begin graph\n"
        "   size 10 8\n"
        "   xtitle \"X\"\n"
        "   ytitle \"Y\"\n"
        "   xaxis min 0 max 3000\n"
        "   yaxis min 0 max 1\n"
        "end graph\n"
        "set hei 0.7\n"
        "set just left\n"
        "amove xg(40) yg(0.34)\n"
        "write \"beta\"\n"
        "set just left\n"
        "amove xg(40) yg(0.09)\n"
        "write \"alpha\"\n"
        "amove xg(2000) yg(0.6)\n"
        "write \"{\\it{T}} = 0.1 K\"\n"
    )
