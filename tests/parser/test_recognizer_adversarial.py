# Finding 16 -- a blank line between text clusters (or between 'end graph'
# and the first cluster) aborted _consume_text_cluster's ENTIRE scan on real
# user data, silently dumping every cluster from that point on -- including
# already-matched-looking ones -- into fig.passthrough_trailer.
#
# Root cause: _try_one_text(nodes, i) is handed a BlankOrComment node at
# ``i`` whenever a blank line separates two clusters (a hand-edited file, not
# anything the writer itself emits -- see writer.add_text, which never
# inserts blank lines between clusters). _as_statement(BlankOrComment) always
# returned None, so the mandatory 'amove xg()/yg()' check failed immediately
# and _try_one_text returned (i, None) with no attempt to look past the
# blank line. _consume_text_cluster's outer while-loop then breaks on the
# very first failure, so EVERYTHING from that blank line onward -- the blank
# line itself, plus the next cluster's amove/write lines, one node per loop
# iteration in the top-level driver -- lands in passthrough_trailer. This is
# why a file with the blank line positioned right before the LAST cluster
# shows a 3-entry trailer for what looks like 2 physical lines: the blank
# line is entry 1, the final 'amove xg(...) yg(...)' is entry 2, and the
# final 'write "..."' is entry 3 (three separate Node objects each pushed
# individually by ``trailer.extend(self._raw_lines(node))`` in ``run()``).
#
# Explicitly NOT the cause (verified empirically before fixing): trailing
# whitespace after the closing quote, CRLF line endings, and a missing final
# newline at EOF are all handled correctly by the lexer/syntax layers already
# -- none of those three break _try_one_text in isolation. Only the blank
# line does.
#
# Fix: _try_one_text now calls _skip_blanks() to tolerate a run of
# BlankOrComment nodes before each mandatory element (the 'set' run, the
# 'amove', and the 'write'), but only PROVISIONALLY -- if the pattern still
# fails to complete, the ORIGINAL index (not the blank-skipped one) is
# returned, so the blank line and everything after it still falls through to
# ordinary passthrough handling untouched when it is NOT actually a text
# cluster.
# --------------------------------------------------------------------------- #

def test_finding16_blank_line_before_final_cluster_is_recognized(tmp_path):
    """Reproduces the reported real-file shape (synthetic recreation): a
    graph, an earlier cluster that already parses fine, a blank line, then a
    FINAL cluster whose write string has a trailing space after the closing
    quote and is the last line of the file. Before the fix, the blank line
    aborted the scan and the final cluster's amove+write leaked into
    passthrough_trailer (3 entries: the blank line + the 2 leaked
    statements). After the fix, both clusters recognize and the trailer is
    empty.
    """
    src = (
        "size 10 8\n"
        "begin graph\n"
        "   size 10 8\n"
        "   xaxis min 0 max 3000\n"
        "   yaxis min 0 max 1\n"
        "end graph\n"
        "amove xg(500) yg(0.9)\n"
        'write "first"\n'
        "\n"
        "amove xg(2000) yg(0.6)\n"
        'write "{\\it{T}} = 0.1 K" \n'  # trailing space after the closing quote
    )
    gle = _write(tmp_path, "blankcluster.gle", src)
    rec = parse_gle_figure(gle)
    ax = rec.figure.axes_list[0]

    assert rec.figure.passthrough_trailer == []
    assert len(ax.texts) == 2
    first, temp = ax.texts
    assert first["text"] == "first"
    assert temp["x"] == pytest.approx(2000.0)
    assert temp["y"] == pytest.approx(0.6)
    # The trailing space after the closing quote is NOT part of the string
    # token (it lies outside the quotes) -- recovered text is exactly the
    # quoted content, byte-exact.
    assert temp["text"] == "{\\it{T}} = 0.1 K"


@pytest.mark.parametrize(
    "ending,final_newline",
    [
        ("\n", True),
        ("\n", False),
        ("\r\n", True),
        ("\r\n", False),
    ],
    ids=["lf_with_eof_nl", "lf_no_eof_nl", "crlf_with_eof_nl", "crlf_no_eof_nl"],
)
def test_finding16_eof_and_line_ending_variants_all_recognized(
    tmp_path, ending, final_newline
):
    """The blank-line fix must hold regardless of line-ending style and
    regardless of whether the file ends with a trailing newline -- these
    axes are independent of the blank-line bug but are verified together
    here since they were the other real-file variants under suspicion.
    """
    lines = [
        "size 10 8",
        "begin graph",
        "   size 10 8",
        "   xaxis min 0 max 3000",
        "   yaxis min 0 max 1",
        "end graph",
        "amove xg(500) yg(0.9)",
        'write "first"',
        "",
        "amove xg(2000) yg(0.6)",
        'write "{\\it{T}} = 0.1 K" ',
    ]
    text = ending.join(lines)
    if final_newline:
        text += ending
    path = tmp_path / "eofvariant.gle"
    with open(path, "w", newline="") as f:
        f.write(text)

    rec = parse_gle_figure(path)
    ax = rec.figure.axes_list[0]

    assert rec.figure.passthrough_trailer == []
    assert len(ax.texts) == 2
    assert ax.texts[0]["text"] == "first"
    assert ax.texts[1]["text"] == "{\\it{T}} = 0.1 K"


