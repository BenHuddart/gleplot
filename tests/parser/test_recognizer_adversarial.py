def test_comment_header_vouched_sidecar_does_not_adopt_comment_names(tmp_path):
    """Comment-line column-header recovery (gleplot.dataio) must NOT feed the
    recognizer's metadata-vouch path: a VOUCHED sidecar whose ONLY column
    names come from a trailing COMMENT line (no inline header row at all)
    must behave EXACTLY like an ordinary headerless sidecar with no comment
    at all -- the comment-derived names must never be adopted as
    ``column_names`` for regeneration.

    Why this matters: gleplot.dataio.load_data_file now recovers
    column_names from a trailing comment line when there is no inline
    header (display-name recovery for the Data dock). But
    _recovered_column_names (recognizer.py) is ALSO used to recover a
    VOUCHED import series' column_names for regeneration on next save --
    if it read comment-derived names too, a hand-edited vouched sidecar
    with a comment header would have those names adopted verbatim and
    (per the existing Critical-1 invariant) re-emitted as a brand-new
    INLINE header line reproducing that exact text on next save --
    changing the file's bytes for a "just display names" feature that is
    supposed to be read-only with respect to save output. The guard
    (`if not table.has_header: return None`) already prevents this since
    comment-derived recovery always leaves has_header False.

    Note: a headerless sidecar (with or without a comment line) already
    gets a *stable default* header line regenerated on next save (see
    ``Axes._default_column_names``, "pre-Track-E3 projects" fallback --
    documented, pre-existing behavior, not something this feature changes
    or should suppress). The invariant this test pins is narrower and
    exact: the regenerated header must be the SAME stable default in both
    cases, i.e. comment-header recovery makes no observable difference to
    what the recognizer/writer produce.
    """

    def _round_trip(dat_content: str, gle_name: str, dat_name: str):
        src = (
            "! GLE graphics file\n"
            "! Generated by gleplot\n"
            "! gleplot-meta-begin v1\n"
            f"! gleplot: import-data = {dat_name}\n"
            "! gleplot-meta-end\n"
            "size 20.32 15.24\n"
            "begin graph\n"
            f'   data {dat_name} d1=c1,c2\n'
            "   d1 line color blue lwidth 0.05\n"
            "end graph\n"
        )
        gle = _write(tmp_path, gle_name, src, {dat_name: dat_content})
        rec = parse_gle_figure(gle)
        ax = rec.figure.axes_list[0]
        assert len(ax.lines) == 1
        return ax, rec

    # (a) No comment line at all -- ordinary headerless sidecar; this is the
    # existing, well-understood baseline behavior.
    ax_plain, _ = _round_trip("0 0\n1 1\n", "plain.gle", "data_plain.dat")

    # (b) Same data, but with a trailing comment line that WOULD qualify as
    # a comment-derived header at the dataio level (table.column_names ==
    # ['x', 'y'], header_source == 'comment').
    ax_comment, rec_comment = _round_trip("! x y\n0 0\n1 1\n", "owned3.gle", "data_9.dat")

    # Confirm the dataio-level recovery actually fired for sanity (the test
    # would be vacuous otherwise).
    from gleplot.dataio import load_data_file

    table = load_data_file(tmp_path / "data_9.dat")
    assert table.has_header is False
    assert table.column_names == ["x", "y"]
    assert table.header_source == "comment"

    # The recognizer's regenerated column_names must be IDENTICAL between
    # the plain and comment-header cases -- the comment text made no
    # difference to what gets adopted for regeneration.
    assert ax_comment.lines[0].get("column_names") == ax_plain.lines[0].get(
        "column_names"
    )

    # End-to-end: re-saving the comment-header case must reproduce the
    # stable default header line, not the original '! x y' comment text
    # promoted to an inline header.
    out_dir = tmp_path / "resaved"
    out_dir.mkdir()
    rec_comment.figure.savefig_gle(str(out_dir / "owned3.gle"))
    resaved = (out_dir / "data_9.dat").read_text(encoding="utf-8")
    first_line = resaved.splitlines()[0]
    assert first_line == "x y"
    assert not first_line.startswith("!")

    # (c) Same safety property for the newer INDEXED '! c N = name' block
    # (gleplot.dataio._recover_indexed_comment_header): a vouched sidecar
    # whose only names come from a 'c 1 = x' / 'c 2 = y' comment block must
    # ALSO behave exactly like the plain headerless case -- these names are
    # unambiguous (unlike the positional last-line heuristic) but are still
    # comment-derived, so the same has_header-gated safety applies.
    ax_indexed, rec_indexed = _round_trip(
        "! c 1 = x\n! c 2 = y\n0 0\n1 1\n", "owned4.gle", "data_10.dat"
    )

    table_indexed = load_data_file(tmp_path / "data_10.dat")
    assert table_indexed.has_header is False
    assert table_indexed.column_names == ["x", "y"]
    assert table_indexed.header_source == "comment"

    assert ax_indexed.lines[0].get("column_names") == ax_plain.lines[0].get(
        "column_names"
    )

    rec_indexed.figure.savefig_gle(str(out_dir / "owned4.gle"))
    resaved_indexed = (out_dir / "data_10.dat").read_text(encoding="utf-8")
    first_line_indexed = resaved_indexed.splitlines()[0]
    assert first_line_indexed == "x y"
    assert not first_line_indexed.startswith("!")


