"""Regression tests for the adversarial-review fixes to the GLE recognizer.

Each test reproduces a specific finding's bug (using the reviewer's repro
snippets) and asserts the fixed behavior. Findings are numbered to match the
review. The byte-identical fixed-point guard (``test_fixed_point.py``) covers
gleplot-authored files; these target *hand-written* tolerance.
"""

from __future__ import annotations

from pathlib import Path

import numpy as np
import pytest

from gleplot import Figure
from gleplot.parser.recognizer import parse_gle_figure


def _write(tmp_path: Path, name: str, content: str, dats: dict | None = None) -> Path:
    for dat_name, dat_content in (dats or {}).items():
        (tmp_path / dat_name).write_text(dat_content, encoding="utf-8")
    p = tmp_path / name
    p.write_text(content, encoding="utf-8")
    return p


def _meta(*import_dats: str) -> str:
    """A ``! gleplot`` metadata block vouching ``import_dats`` as import
    sidecars.

    After the Finding-1 conservative-classification fix, a ``data`` reference
    is only adopted as an editable *import* series (arrays loaded, header
    recovered as column_names) when the metadata block's import-data list
    vouches for it. Hand-written .gle files with no such block are always
    treated as external references. Tests that exercise import-mode recovery
    (multi-line dN accumulation, auto/positional column mapping, literal
    error-bar conversion, ...) prepend this block so their data files are
    genuinely gleplot-owned, matching how gleplot's own writer marks them.
    """
    listed = ", ".join(import_dats)
    return (
        "! GLE graphics file\n"
        "! Generated by gleplot\n"
        "! gleplot-meta-begin v1\n"
        f"! gleplot: import-data = {listed}\n"
        "! gleplot-meta-end\n"
    )


