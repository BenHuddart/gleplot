    rec = parse_gle_figure(gle)
    ax = rec.figure.axes_list[0]

    # All three clusters recognized -- nothing left over in passthrough.
    assert len(ax.texts) == 3
    assert ax.passthrough == []
    assert rec.figure.passthrough_trailer == []

    from gleplot.parser.units import fontsize_cm_to_pt

    expected_fontsize = fontsize_cm_to_pt(0.7)  # 0.7cm -> pt (28.35 divisor)
    assert expected_fontsize == pytest.approx(19.845)

    beta, alpha, temp = ax.texts

    assert beta["x"] == pytest.approx(40.0)
    assert beta["y"] == pytest.approx(0.34)
    assert beta["text"] == "beta"
    assert beta["ha"] == "left"
    assert beta["fontsize"] == pytest.approx(expected_fontsize)

    assert alpha["x"] == pytest.approx(40.0)
    assert alpha["y"] == pytest.approx(0.09)
    assert alpha["text"] == "alpha"
    assert alpha["ha"] == "left"
    # Sticky: no 'set hei' before this cluster -> inherits the 0.7cm value.
    assert alpha["fontsize"] == pytest.approx(expected_fontsize)

    # Third cluster has NO 'set' statements at all -- sticky hei/just/color
    # from the earlier clusters carry forward.
    assert temp["x"] == pytest.approx(2000.0)
    assert temp["y"] == pytest.approx(0.6)
    assert temp["ha"] == "left"
    assert temp["fontsize"] == pytest.approx(expected_fontsize)
    assert temp["color"] == "BLACK"
    # Byte-exact string fidelity: braces and the backslash-escape-looking
    # '\it' must survive completely unmodified (GLE inline-TeX italics).
    assert temp["text"] == "{\\it{T}} = 0.1 K"


