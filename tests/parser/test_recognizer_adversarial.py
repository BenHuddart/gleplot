def test_finding16_blank_line_then_unrelated_content_stays_passthrough(tmp_path):
    """A blank line followed by content that is NOT a valid text cluster
    (e.g. a dangling 'set color' with nothing after it) must still fall
    through to passthrough_trailer untouched -- the blank-line tolerance
    must not turn genuinely non-cluster content into a swallowed/dropped
    line. This pins the 'provisional skip, restore original index on
    failure' half of the fix.
    """
    src = (
        "size 10 8\n"
        "begin graph\n"
        "   size 10 8\n"
        "   xaxis min 0 max 10\n"
        "   yaxis min 0 max 10\n"
        "end graph\n"
        "amove xg(1) yg(1)\n"
        'write "first"\n'
        "\n"
        "set color blue\n"
    )
    gle = _write(tmp_path, "danglingset.gle", src)
    rec = parse_gle_figure(gle)
    ax = rec.figure.axes_list[0]

    assert len(ax.texts) == 1
    assert ax.texts[0]["text"] == "first"
    # The blank line + the dangling 'set color blue' (no following amove)
    # are preserved verbatim in the trailer, not silently dropped.
    assert rec.figure.passthrough_trailer == ["", "set color blue"]


def test_finding16_resave_is_byte_exact_fixed_point(tmp_path):
    """Parse -> save -> parse -> save must be a fixed point once the blank-
    line-separated clusters are recognized (the recovered figure has no
    blank lines between its own clusters, since the writer never emits
    them -- see writer.add_text -- so re-emission is canonical, not a replay
    of the original blank-line formatting)."""
    src = (
        "size 10 8\n"
        "begin graph\n"
        "   size 10 8\n"
        "   xaxis min 0 max 10\n"
        "   yaxis min 0 max 10\n"
        "end graph\n"
        "amove xg(1) yg(1)\n"
        'write "first"\n'
        "\n"
        "amove xg(2) yg(2)\n"
        'write "{\\it{T}} = 0.1 K" \n'
    )
    gle = _write(tmp_path, "fixedpoint.gle", src)
    rec1 = parse_gle_figure(gle)
    out1 = tmp_path / "out1.gle"
    rec1.figure.savefig_gle(str(out1))
    text1 = out1.read_text(encoding="utf-8")

    rec2 = parse_gle_figure(out1)
    out2 = tmp_path / "out2.gle"
    rec2.figure.savefig_gle(str(out2))
    text2 = out2.read_text(encoding="utf-8")

    assert text1 == text2
    assert rec2.figure.axes_list[0].texts[1]["text"] == "{\\it{T}} = 0.1 K"
